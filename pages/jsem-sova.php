<h1>Jsem sova: Proč můj vnitřní procesor bootuje až odpoledne</h1>

<p>V našem světě orientovaném na výkon se ranní vstávání glorifikuje jako základní stavební kámen úspěchu. „Ranní ptáče dál doskáče,“ říká se. Jako programátor a řemeslník, který si zakládá na datech a výsledcích, jsem se dlouho snažil tento model adoptovat. Jenže realita mého biologického hardwaru má jinou specifikaci.</p>

<p>Pravda je taková, že jsem sova. A není to projev lenosti, ale otázka efektivity a využití plného potenciálu.</p>

<h2>Škola jako zóna ranního útlumu</h2>

<p>Moje nenávist k rannímu vstávání má hluboké kořeny v dobách školní docházky. Pamatuji si ten pocit marnosti, když zazvonil budík. Sice jsem dokázal fungovat – když jsem se k tomu dokopal, zvládl jsem napsat písemku hned první hodinu na jedničku (když jsem se teda předtím učil...), ale za jakou cenu?</p>

<p>Ve škole jsem se většinou nudil. Ten standardizovaný čas od osmi do dvou pro mě byl spíše pasivním přežíváním. Skutečně myslet, tvořit a tedy v mých očích „žít“, jsem začínal až ve chvíli, kdy odpoledne vyučování skončilo. Tehdy se můj vnitřní systém konečně přepnul do ostrého režimu.</p>

<h2>Dopolední režim: Nízká zátěž procesoru</h2>

<p>V profesní praxi jsem se naučil se svým biorytmem pracovat. Vím, že dopoledne ze sebe nedostanu hlubokou analytickou práci nebo řešení komplexních architektonických rébusů. Je to čas na „lehké úkoly“. </p>

<p>Moje dopoledne vypadá takto:</p>

<ul>
    <li>Odepisování na zprávy a e-maily.</li>
    <li>Kreslení myšlenkových map.</li>
    <li>Administrativa, která nevyžaduje stoprocentní kreativní focus.</li>
</ul>

<p>Je to takový můj „warm-up“. Stejně jako stroj potřebuje dosáhnout provozní teploty, i moje mysl se postupně rozjíždí. Kolem oběda často přichází útlum, kdy si musím na chvíli zdřímnout – krátký restart systému, který mi umožní fungovat ve špičkovém výkonu v druhé polovině dne.</p>

<h2>Když se dílna otevře v noci</h2>

<p>Když se konečně dostanu do svého tempa, čas pro mě přestává existovat. Pokud mě projekt pohltí, dokážu programovat i do čtyř nebo pěti do rána. V tom tichu noci, kdy okolní svět spí, vládne v mé dílně největší klid na práci. </p>

<p>Samozřejmě, jako řemeslník vím, že takové „overclockování“ organismu nelze dělat denně. Dlouhodobě to není udržitelné a vede to k technickému dluhu na vlastním zdraví. Proto se neustále snažím o nastavení životosprávy, která by mi umožnila co nejstabilnější denní cyklus.</p>

<h2>Rozpor mezi směrnicí a potenciálem</h2>

<p>Můj přirozený cyklus se bohužel od společenského standardu dosti liší. Adaptace na „běžnou pracovní dobu“ je pro mě vždycky bojem. </p>

<p>Dokážu se přizpůsobit. Jako profesionál dokážu vstát „podle směrnice“, když to projekt vyžaduje. Dokážu držet pohotovost a být na příjmu, když hoří termín. Ale musím být upřímný: v takovém režimu nevyužívám svůj potenciál naplno. Je to jako jet se sportovním autem v zácpě na jedničku. Motor běží, ale k čemu to je?</p>

<p>Věřím, že profesionalita programátora spočívá i v tom, že zná své nástroje – a tím nejdůležitějším nástrojem je jeho vlastní mysl. Pokud vím, že nejlepší kód odevzdám v sedm večer a ne v osm ráno, je mou řemeslnou povinností si pracovní workflow nastavit tak, aby klient dostal tu nejvyšší kvalitu.</p>

<p>Být sova není chyba v kódu. Je to jen jiná architektura dne.</p>