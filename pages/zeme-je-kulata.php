<h1>Země je kulatá. Američané na Měsíci přistáli. Chemtrails jsou zkondenzovaná vlhkost. Nový světový řád neexistuje. Nastal druhý příchod Ježíše Krista.</h1>

<h2>Možná nejsem normální, ale rozhodně nejsem hloupý</h2>

<p>Po většinu života mě mnoho lidí ve spoustě věcí velmi podceňovalo. Ovšem přes všechny útoky na moji osobu, fyzické i verbální, na jedné věci jsem si opravdu
zakládal – <strong>na své schopnosti přemýšlet</strong>. Možná nejsem extrémně chytrý, ale snažím se věci vždy důkladně analyzovat a vyhodnocovat. Díky tomu jsem schopen
přijít na řadu věcí, které při letmém pohledu nejsou zřejmé.</p>

<h2>Kdo může věřit, že Země je plochá?</h2>

<p>Naše paní učitelka dějepisu nám říkala, že si je jistá, že Země je kulatá, protože mnoho lidí v historii její kulatost potvrdilo. Přesto však řekla: <em>„Kdyby historii nestudovala a tato informace se nestala všeobecně rozšířenou, jak bychom mohli poznat, že je to pravda?"</em></p>

<p>Pohyb Země necítíme, tak si můžeme snadno myslet, že Slunce obíhá kolem Země stejně jako ostatní hvězdy i planety. Dnes už ale máme <strong>více než dostatek důkazů</strong>, že heliocentrický systém je skutečný. Vezmeme-li v potaz všechny známé veličiny, hmotnosti, vzdálenosti a vložíme je do správných rovnic, zjistíme, že celý tento systém dává smysl a nemá jedinou slabinu.</p>

<p>Naopak teorie ploché Země je absurdní už v tom ohledu, že její zastánci se ani přesně neshodnou v tom, jak by vysvětlili přírodní úkazy, které tuto teorii zpochybňují.
A <strong>kdyby Země byla opravdu plochá, co by kdo měl z toho, že nám bude tajit takovou věc?</strong> Mně jakožto obyčejnému člověku, který ani moc necestuje, by to vlastně mohlo být
úplně fuk, jestli žiji na kouli nebo na ploše.</p>

<p>Jediný jakýsi relevantní argument je, že ona „zeměplocha" není ještě zcela prozkoumaná, respektive existují oblasti, které se nezakreslují na žádné mapy a které disponují
přírodním bohatstvím nebo slouží jako místo pro úkryt něčeho, co elity nechtějí, abychom věděli. Ale upřímně – <em>není i tak dost míst, kde se dá leccos skrýt, i když na
mapě jsou?</em></p>

<h2>Jak bych zfalšoval přistání na Měsíci já?</h2>

<p>Je pravdou, že v době studené války měly Spojené státy silný motiv přistání na Měsíci zfalšovat, pokud by seznaly, že to nedokážou reálně provést. Jenže jak by toho
byly schopny? <strong><em>Než zfalšovat přistání na Měsíci, bylo by jednodušší tam skutečně přistát.</em></strong></p>

<p>Jeden kamarád mi řekl, že nějaký jeho kamarád objevil na internetu aplikaci, která umožňuje odstranit z fotky nebo videa speciální efekty a zjistit tak, jak úplně původně vypadala. Jako příklad jedné takové fotky mi ukázal slavnou
fotku z Měsíce, kde Edwin Aldrin stojí u vlajky USA na Měsíci a přidržuje ji <em>zelený mužík</em>, kterého potom digitálně odstranili. Z této fotky mi bylo vyloženě smutno.
To je něco tak absurdního, že jsem začal přemýšlet, co bych dělal, kdybych já byl na místě těch lidí, kteří měli za úkol zfalšovat přistání na Měsíci.</p>

<blockquote>
<p><strong>Na programu Apollo pracovalo přes čtyři sta tisíc lidí.</strong> Jak bych mohl donutit všechny tyto lidi, aby mlčeli o tom, že přistání na Měsíci bylo zfalšováno?</p>
</blockquote>

<p>Pochopitelně, ne všichni by o tom museli vědět. Zaměstnanci firmy, která dodávala šroubky do kosmické lodi, o tom vědět nemusela. Stejně tak teoreticky i samotní
konstruktéři z Grummanu, North American Aviation a jiných společností to nemuseli vědět. Oni by prostě dodali letový hardware, dostali by zaplaceno a už by se jim neřeklo, že byl tajně
sešrotován.</p>

<p>Jenže víte, co by byla sranda? <strong>Kdyby nějaký inženýr zpětně přišel s tím, že udělal nějakou chybu</strong>, která by znamenala s největší pravděpodobností selhání
nějakého důležitého systému a nechápal by, jak to, že k ničemu takovému nedošlo. V programu Apollo se reálně naprostá většina systémů designovala úplně na doraz a je
naprostý zázrak, že došlo jen ke dvěma závažným nehodám, přičemž pouze jedna skončila tragicky – Apollo 1.</p>

<p>Pokud bych přistání na Měsíci falšoval, s největší pravděpodobností by oficiálně k žádné tragické nehodě nedošlo a nejspíš nedošlo ani k žádnému závažnému
selhání. Apollo 11 by určitě nebylo tak dramatické a naopak by bylo hodně nudné. Astronauti by přistáli, udělali by pár fotek, nasbírali vzorky a jeli by zpátky.</p>

<p>Jelikož však cesty na Měsíc se velmi rychle staly nudnou záležitostí, možná by nebylo od věci sehrát dramatickou misi jako bylo Apollo 13. Současně by to taky bylo
jakési eso v rukávu pro případ, že bych chtěl oznámit důvod, proč se na Měsíc od té doby neletělo. Mimochodem, Apollo 13 byl reálně velmi silný argument, proč program
Apollo neměl žádné pokračování. Hlavním důvodem ale byla politika a finance.</p>

<blockquote>
<p><strong>A konečně, nejsilnější argument:</strong> Sovětský svaz přistání na Měsíci nikdy nezpochybnil.</p>
</blockquote>

<p>Ve skutečnosti, podle některých statistik, největší podíl lidí, kteří přistání na Měsíci zpochybňují, jsou Američané. A mnoho z nich se dokonce setkalo s astronauty, kteří
na Měsíci byli. <strong>Kdyby Sověti měli sebemenší podezření, že se jedná o podvod, byli by první, kdo by zvedl ruku a řval.</strong> To, že Sověti mlčeli, je ten nejlepší
a nejspolehlivější důkaz.</p>

<h2>Někdo vzal až příliš vážně Jožina z bažin</h2>

<p>Je pravdou, že existují letadla, která jsou přímo určená k rozprašování různých materiálů do ovzduší. Tato letadla se používají k nanášení hnojiv na zemědělskou půdu
nebo k různým postřikům proti škůdcům.</p>

<p>Věřte mi ale, že <strong>je naprostý nesmysl, aby dopravní letadla záměrně rozprašovala nějaké chemikálie, které nás mají ovládat</strong>. Nejen, že takové chemikálie s největší pravděpodobností neexistují, ale i kdyby existovaly, rozprašovat je letadly je ten nejblbější způsob, jak s nimi ovládnout lidi.</p>

<p><em>Nebylo by o mnoho řádů levnější je pašovat do potravin nebo vody?</em> A jak by vlastně takové chemikálie fungovaly? To jako kdo se toho nadýchá, tak potom přijímá nějaký
tajný signál, který mu uděluje příkazy, aby se dotyčný choval iracionálně ve prospěch členů nějaké tajné elity?</p>

<p>Už ta skutečnost, že je tu tolik lidí, kteří nadávají na to, že nás elity ovládají nebo chtějí ovládat, je důkazem toho, že to prostě nejde. <strong>Pokud by nás skutečně někdo ovládal, to nejdůležitější pro něj by bylo, aby si
nikdo neuvědomoval, že je ovládán.</strong> Jediné, z čeho můžeme vinit chemtrails, respektive kondenzační stopy za letadly, je to, že
zaměstnávají mozky celé řady blbců, kteří nemají nic zajímavějšího na práci než vymýšlet důvody, proč se máme špatně.</p>

<h2>Ale upřímně, kdybychom byli pod nadvládou nějakých tajných elit, co by to pro nás znamenalo a jak by to reálně fungovalo?</h2>

<p>Nebavme se tady o totalitních režimech. Bavme se tu skutečně o tom, kdyby nad námi měl někdo absolutní moc a my bychom o tom ani nevěděli. Zde si můžu vypůjčit
jednu známou myšlenku – film <strong>Matrix</strong>. V tomto filmu jsou lidé uvězněni v simulované realitě, zatímco jejich těla jsou využívána jako zdroj energie pro
stroje. Kdybychom ale byli opravdu v takové situaci, jak by to pro nás reálně vypadalo? Většina z nás by žila svůj běžný život, chodila by do práce, starala by 
se o rodinu, bavila by se s přáteli. <em>Nikdo by si ničeho nevšiml.</em></p>

<p>Ve filmu je však zmíněná jedna podstatná myšlenka. Agent Smith říká, že první Matrix byl dokonalý. Lidé v něm žili v ráji, neměli žádné problémy, nic je netrápilo.
Ale lidé se vzbouřili, protože <strong>lidský mozek není stavěný na to, aby lidé žili v absolutním štěstí</strong>. Lidé potřebují utrpení, jinak žijí ve snu, ze kterého se chtějí probudit.
A to je přesně ten důvod, proč by nás žádná tajná elita nikdy nemohla ovládat. My sami bychom se proti tomu vzbouřili, protože lidská přirozenost je taková, že lidé
potřebují určitou míru stresu a utrpení, jinak si je začnou uměle vytvářet.</p>

<p>Vždyť se podívejme na realitu dnešní doby, která není nijak skrytá. <strong>Žijeme v takovém nadbytku, že si lidé uměle vytvářejí stresové situace, jen aby měli pocit,
že žijí.</strong> Není to ale zvláštnost dnešní doby. Už ve starém Římě byla hrstka vyvolených, kteří měli všeho dostatek, zatímco většina lidí žila v bídě. A tito vyvolení
byli pravděpodobně stejně zpovykaní jako dnešní většinová populace a tak vymýšleli blbosti.</p>

<p>Já neříkám, že se člověk nemá bavit a že by měl splynout s přírodou a žít jako mnich nebo poustevník. V dnešní době ale je potřeba, aby si člověk uměl ten svůj život
zorganizovat, aby věděl, <em>co je pro něj důležité a co ne</em>. Aby si uměl stanovit priority a nenechal se unášet proudem, který mu vnucují média, reklamy a taky ďábelsky
sofistikované algoritmy sociálních sítí.</p>

<h2>Nastal druhý příchod Ježíše Krista</h2>

<p>Pokud jste dočetli až sem, možná si říkáte, že jsem asi striktně racionální člověk a snažím se všechno vysvětlovat exaktně. To je pravda, ale věřte mi nebo ne,
zjistil jsem, že <strong>osoba Ježíše Krista je stejně exaktní jako kvantová fyzika</strong>. Jen je třeba se na to podívat ze správného úhlu a vyhnout se balastu, což je, pravda,
v tomto oboru náročnější a složitější než ve většině ostatních oborů.</p>

<p>Dovolte mi jednoduché přirovnání. Jednou z nejpřesnějších a nejracionálnějších věd, které dnes máme, je <strong>matematika</strong>. Matematika je založená na přesných pravidlech,
které se nazývají axiomy. Z těchto axiomů potom vyplývají další pravidla, která nám umožňují řešit složité problémy. <em>Základní soubor pravidel v teologii je Bible.</em>
Přestože je poměrně dlouhá a složitá, ve skutečnosti se v ní nachází přesné a neměnné principy, na základě kterých uvažuje a koná ten, kdo ji napsal – tedy Bůh.</p>

<blockquote>
<p><strong>Otázka existence Ježíše Krista a jeho druhého příchodu totiž není o víře, ale o poznání.</strong></p>
</blockquote>

<p>Já jsem nebyl vychován v křesťanské víře a pokud by někdo sledoval můj soukromý život, tak by mě ani za křesťana nepovažoval. Pokud jsme se v životě setkali s nějakým člověkem, určitě si budeme jistí jeho existencí. V jeho existenci
budeme jistě plně věřit i v případě, že ho například uvidíme v televizi. Určitě by nikdo nezpochybňoval existenci Donalda Trumpa, i když se s ním osobně nepotkal.
Mnoho lidí ale také říká, že si myslí, že Bůh neexistuje, protože se jim nelíbí, co říká, respektive, co si o něm myslí. <em>To je přece nesmysl. Pokud s někým nesouhlasíme,
nemůžeme přece tvrdit, že neexistuje.</em></p>

<p><strong>A jak si můžu být tak jistý existencí Ježíše Krista a jeho druhým příchodem? Já jsem se s ním setkal.</strong> Bylo sice trochu obtížnější poznat, že je to opravdu on,
ale nyní již vím, že je to nezpochybnitelné. On mi totiž vysvětlil (a nejen mně, ale řadě dalších lidí, se kterými se stýkám), co které pasáže v Bibli znamenají, jak je
máme chápat a především, co sdělují přímo nám, kteří žijeme v dnešní době.</p>

<p>Byla zde jedna věc, které jsem nevěřil. Potom, co jsem přijal všechno tohle poznání, mi bylo divné, jak to, že za celých dva tisíce let nikdo nedokázal ten pravý význam rozluštit. Skutečně, <strong>při výkladu nebyl použit žádný jiný zdroj než samotná Bible</strong>. Nebylo nic,
kde bychom si museli pomáhat nějakým jiným zdrojem. Jediné, co jsme potřebovali, bylo chápat, jak fungují dané věci fyzicky a proč jsou použité v dané situaci jako
metafora.</p>

<p>Pravda – jsou tam věci, se kterými se v moderní době nesetkáme. Dnes už málokdy používáme svícny či olejové lampy, nepoužíváme tažná zvířata a nejsme obeznámeni,
při jaké příležitosti se lidé oblékali do žíněných šatů (používaly se na pohřbech a při smutných událostech). Ale když pochopíme, jak to tehdy fungovalo, rázem nám dává
smysl i ono přirovnání, které je pod danou pasáží skryté.</p>

<h3>Jeden příklad za všechny:</h3>

<blockquote>
<p><strong>Zjevení 6:12-14:</strong> <em>„A když otevřel šestou pečeť, uviděl jsem, že nastalo veliké zemětřesení, slunce zčernalo jako žíněný pytel a měsíc úplně 
zkrvavěl. A nebeské hvězdy spadly na zem, jako když fíkovník zmítaný vichrem shazuje své pozdní fíky; a nebe zmizelo, jako když se svinuje svitek, a každá hora i každý
ostrov se pohnuly ze svého místa."</em></p>
</blockquote>

<p>Na první pohled to vypadá jako popis nějaké apokalyptické události. Ale pokud bych vám řekl, že:</p>

<ul>
<li><strong>slunce</strong> znamená pastor nebo hlava církve</li>
<li><strong>měsíc</strong> znamená evangelista</li>
<li><strong>hvězdy</strong> znamenají členy církve</li>
<li><strong>nebe</strong> znamená duchovní svět</li>
<li><strong>země</strong> znamená svět fyzický</li>
<li><strong>hora</strong> znamená denominaci</li>
<li><strong>ostrov</strong> znamená jednotlivé sbory</li>
</ul>

<p>...najednou by to nebylo vlastně nic nadpřirozeného. Ten výklad je trošku složitější, ještě by bylo potřeba zmínit, že slunce vyzařuje světlo nebo že nebe je výš než země, ale teď jen ve stručnosti.
<strong>Sdělení této pasáže je, že tu je velká církev, která však padne včetně jejího vedení a všech jejích členů</strong>, což se jistě může stát a taky se to stalo. A o které církvi
se tu píše? Na to se poukazuje v dalších pasážích. <em>(spoiler alert: Není to katolická církev)</em></p>

<p>Takových příkladů bych mohl uvést mnoho. A co je nejdůležitější, <strong>všechny tyto pasáže dávají smysl v kontextu událostí, které se skutečně staly</strong>. Se svými bratry
a sestrami jsme prošli skrz na skrz celou knihu Zjevení a přestože je to velmi dlouhý a složitý soupis událostí, vše do sebe zapadá jako puzzle. A to je ten důvod,
proč vím, že Ježíš Kristus skutečně existuje a že jeho druhý příchod nastal.</p>
